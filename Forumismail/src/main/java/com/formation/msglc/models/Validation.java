package com.formation.msglc.models;

public class Validation {
	private String [] categories;
	private String [] sousCategories;
	private String [] sujets;
	private String [] reponses;
	public String[] getCategories() {
		return categories;
	}
	public void setCategories(String[] categories) {
		this.categories = categories;
	}
	public String[] getSousCategories() {
		return sousCategories;
	}
	public void setSousCategories(String[] sousCategories) {
		this.sousCategories = sousCategories;
	}
	public String[] getSujets() {
		return sujets;
	}
	public void setSujets(String[] sujets) {
		this.sujets = sujets;
	}
	public String[] getReponses() {
		return reponses;
	}
	public void setReponses(String[] reponses) {
		this.reponses = reponses;
	}
	

}
